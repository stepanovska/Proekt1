    private void btnDodadiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodadiActionPerformed
        // TODO add your handling code here:
        String Ime = tekstIme.getText();
        String Email = tekstMail.getText();
        String Adresa = tekstAdresa.getText();
        String Telefon = tekstTel.getText();
        
        if (Ime.isEmpty() || Email.isEmpty() || Adresa.isEmpty() || Telefon.isEmpty()){
            JOptionPane.showMessageDialog(this, "Vnesete gi site polinja!","Obidete se povtorno!", JOptionPane.ERROR_MESSAGE);
        } else
        {
            DefaultTableModel model = (DefaultTableModel) tabelaPodatoci.getModel();
            model.addRow (new Object[]{Ime,Email,Adresa,Telefon});
            tekstIme.setText("");
            tekstMail.setText("");
            tekstAdresa.setText("");
            tekstTel.setText("");
        }
    }//GEN-LAST:event_btnDodadiActionPerformed

