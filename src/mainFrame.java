    private void btn2IzbrishiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn2IzbrishiActionPerformed
        // TODO add your handling code here:
        int red = tabelaPodatoci.getSelectedRow();
        
        if(red<0){
            JOptionPane.showMessageDialog(this, "Ne ste odbrale red!Ve molam oberete.", "Odberete red!", JOptionPane.ERROR_MESSAGE);
        } else{
            DefaultTableModel model = (DefaultTableModel) tabelaPodatoci.getModel();
            model.removeRow(red);
        }
    }//GEN-LAST:event_btn2IzbrishiActionPerformed

    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        // TODO add your handling code here:
        DefaultTableModel model=(DefaultTableModel) tabelaPodatoci.getModel();
        Vector<Vector> tableData=model.getDataVector();
        
        try{
            FileOutputStream file= new FileOutputStream("file.bin");
            ObjectOutputStream output= new ObjectOutputStream(file);
            output.writeObject(tableData);
            output.close();
            file.close();
            
        } catch (IOException ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_formWindowClosing

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try{
            FileInputStream file= new FileInputStream("file.bin");
            ObjectInputStream input= new ObjectInputStream(file);
             Vector<Vector> tableData=(Vector<Vector>)input.readObject();
             input.close();
             file.close();
             
             DefaultTableModel model=(DefaultTableModel) tabelaPodatoci.getModel();
             for(int i=0;i<tableData.size();i++){
                 Vector row=tableData.get(i);
                 model.addRow(new Object[]{row.get(0),row.get(1),row.get(2),row.get(3)});
                 
             }
             
             
        } catch(IOException | ClassNotFoundException ex){
              ex.printStackTrace();
        }
    }//GEN-LAST:event_formWindowOpened

